.TEXT ;CODE SEGMENT STARTS HERE
LDR R1,=A ;LOAD ADDRESS OF A INTO R1 ; =A GIVES ADDRESS OF A
LDR R2,=B
LDR R6,=C

LDR R3,[R1] ;LOAD CONTENT FROM R1 ADDRESS'S MEMORY (12345678) ; [R1] GIVES VALUE PRESENT AT THE ADDRESS STORED IN R1
LDR R4,[R2]

ADD R5,R3,R4

STR R5,[R6] ;STORING CONTENT OF R5 INTO MEMORY LOCATION STORED IN R6
SWI 0X011 ;LOGICAL END ; THIS LINE TERMINATES THE PROGRAM

.DATA ;DATA SEGMENT STARTS HERE
A:.WORD 0X12345678
B:.WORD 0X11111111
C:.WORD 0X00000000
;PHYSICAL 
